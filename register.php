    try {
        $stmt = $pdo->prepare("INSERT INTO users (username, password, user_type) VALUES (?, ?, ?)");
        $stmt->execute([$username, $password, $user_type]);
        header("Location: login.php"); // Redirect to login page after registration
        exit();
